export function checkWeChatGroupCommandSafety(text = '') {
  const value = String(text || '').trim()
  if (!value) return { allowed: true }
  const hits = DANGEROUS_RULES.filter(rule => rule.pattern.test(value)).map(({ id, label, severity, description, safeAlternative }) => ({ id, label, severity, description, safeAlternative }))
  if (!hits.length) return { allowed: true }
  return {
    allowed: false,
    hits,
    reason: `为了保护电脑、微信账号、隐私和资金安全，微信群入口禁止执行或读取此类指令：${hits.map(h => h.label).join('、')}。我可以解释风险、整理安全的手动操作步骤，但不会替群成员远程查看、读取或操作机主电脑。`,
  }
}
